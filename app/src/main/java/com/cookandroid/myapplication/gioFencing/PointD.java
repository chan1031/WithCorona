package com.cookandroid.myapplication.gioFencing;
//Double type PointD
public class PointD {
    double x;
    double y;

    PointD(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getY(){
        return y;
    }

    public double getX(){
        return x;
    }

}
